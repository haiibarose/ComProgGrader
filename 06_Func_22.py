import math

def distance1(x1, y1, x2, y2):
# คืนระยะห่างระหว่างจุด (x1,y1) กับ (x2,y2)
# ตัวอย่างการใช:้ d1 = distance1(0.0, 0, 3, 4) ได้ d1 = 5.0
    return math.sqrt((x1-x2)**2+(y1-y2)**2)

def distance2(p1, p2):
# p1 และ p2 เป็นลสิ ต ์
# แต่ละลิสต์แทนจุด ทเี่ ป็นลิสต์ทภี่ ายในมี 2 ช่อง เก็บพิกัด x กับ y
# คืนระยะระหว่างจุด p1 กับ p2
# ตัวอย่างการใช:้ d2 = distance2([0.0, 0], [3, 4]) ได้ d2 = 5.0
    return math.sqrt((p1[0]-p2[0])**2+(p1[1]-p2[1])**2)

def distance3(c1, c2):
# c1 และ c2 แทนวงกลม 2 วง
# แต่ละลิสต์เป็นลิสต์ 3 ช่อง เก็บพกิ ัด x กับ y (ของจุดศูนย์กลาง) และรัศมี
# คืนระยะระหว่างจุดศูนย์กลางของ c1 กับ c2 และค่าจริง/เท็จว่า c1 กับ c2 แตะหรือทับกันหรือไม่
# ตัวอย่างการใช:้ d3,overlap = distance3([0.0, 0, 1], [5, 0, 2])
# ได้ d3 = 5.0, overlap = False
    d = distance2(c1[:2], c2[:2])
    overlap = c1[-1]+c2[-1]>d
    return d, overlap

